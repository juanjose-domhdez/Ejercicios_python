#Operadores Matmaticos

name = "Frank" #asignacion =

print(20 + 33) # suma +
print(20 - 15) # resta -
print(12 * 41) # multiplicacion *
print(12 / 3)  # division flotante /
print(13 // 4) # division entera //
print(13 % 3)  # modulo %
print(2 ** 5)  # potencia **

# Operadores de comparacion (verdadero o falso)
print(10 < 10) # menor que
print(10 > 10) # mayor que
print(10 <= 10) # menor que o igual que
print(10 >= 10) # mayor que o igual que
print(10 == 10) # igual
print(10 != 10) # diferente

# Operadores Logicos
print(True and True) # AND
print(True or False) # OR
print(not True) # NOT