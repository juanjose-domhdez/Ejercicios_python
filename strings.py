# Strings con Python

my_string = "Este es un String"
print(my_string)

other_string = "Este es otro String"
print(other_string)

my_self = "I'm Francisco"
print(my_self)

some = 'Hello is "Hola"'
print(some)

name = "Francisco"
age = 41
city = "Huamantla"

message_1 = "Yo soy " + name + ', vivo en ' + city + " y tengo " + str(age) + " años."
print(message_1)

message_2 = f"Yo soy { name }, vivo en { city } y tengo {age} años"
print(message_2)
